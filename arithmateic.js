//returns false

//equality
console.log(onenum == twonum)
//returns false


//not equal
console.log(onenum != twonum)
//returns true

//javascript is dynamically typed langauge
//most langauges are statically typed

//strictly equals = compares value and data type 
console.log(3 === 3)
//returns true


console.log(3 === '3')
//returns false


//less strict = only compares value
console.log(3 == 3)
console.log(3 == '3')
//both return true